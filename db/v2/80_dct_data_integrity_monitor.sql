DECLARE
  l_exists NUMBER;
  l_start TIMESTAMP WITH TIME ZONE;
BEGIN
  l_start:=TO_TIMESTAMP_TZ(
    TO_CHAR(SYSTIMESTAMP AT TIME ZONE 'Asia/Dubai'+INTERVAL '1' DAY,'YYYY-MM-DD')||
    ' 01:15:00 Asia/Dubai','YYYY-MM-DD HH24:MI:SS TZR');
  SELECT COUNT(*) INTO l_exists FROM dba_scheduler_jobs
   WHERE owner='PROD' AND job_name='DCT_INTEGRITY_JOB';
  IF l_exists=0 THEN
    DBMS_SCHEDULER.CREATE_JOB(
      job_name=>'PROD.DCT_INTEGRITY_JOB',job_type=>'STORED_PROCEDURE',
      job_action=>'PROD.DCT_INTEGRITY_PKG.CHECK_INTEGRITY',start_date=>l_start,
      repeat_interval=>'FREQ=DAILY;BYHOUR=1;BYMINUTE=15;BYSECOND=0',
      enabled=>FALSE,auto_drop=>FALSE,
      comments=>'Daily read-only orphan, duplicate and constraint-validation check');
  ELSE
    DBMS_SCHEDULER.DISABLE('PROD.DCT_INTEGRITY_JOB',force=>TRUE);
    DBMS_SCHEDULER.SET_ATTRIBUTE('PROD.DCT_INTEGRITY_JOB','job_action','PROD.DCT_INTEGRITY_PKG.CHECK_INTEGRITY');
    DBMS_SCHEDULER.SET_ATTRIBUTE('PROD.DCT_INTEGRITY_JOB','start_date',l_start);
    DBMS_SCHEDULER.SET_ATTRIBUTE('PROD.DCT_INTEGRITY_JOB','repeat_interval',
                                 'FREQ=DAILY;BYHOUR=1;BYMINUTE=15;BYSECOND=0');
  END IF;
  DBMS_SCHEDULER.ENABLE('PROD.DCT_INTEGRITY_JOB');
END;
/

BEGIN prod.dct_integrity_pkg.check_integrity; END;
