CREATE OR REPLACE PACKAGE prod.dct_integrity_pkg AUTHID DEFINER AS
  PROCEDURE check_integrity;
  PROCEDURE run_integrity_check(p_checked_by VARCHAR2);
END dct_integrity_pkg;
/

CREATE OR REPLACE PACKAGE BODY prod.dct_integrity_pkg AS
  FUNCTION module_for_table(p_table VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    IF p_table LIKE 'DCT_FL_%' THEN RETURN 'FL';
    ELSIF p_table LIKE 'DCT_CC_%' THEN RETURN 'CC';
    ELSIF p_table LIKE 'DCT_PC_%' THEN RETURN 'PC';
    ELSIF p_table LIKE 'DCT_HR_%' THEN RETURN 'HR';
    ELSIF p_table LIKE 'DCT_AR_%' THEN RETURN 'AR';
    ELSIF p_table LIKE 'DCT_ATD_%' THEN RETURN 'ATD';
    ELSIF p_table LIKE 'DT_%' THEN RETURN 'DT';
    ELSIF p_table LIKE 'DCT_%' THEN RETURN 'ADMIN';
    ELSE RETURN SUBSTR(p_table,1,INSTR(p_table||'_','_')-1);
    END IF;
  END;

  PROCEDURE perform_check(p_checked_by VARCHAR2) IS
    l_sql          VARCHAR2(32767);
    l_join         VARCHAR2(32767);
    l_nonnull      VARCHAR2(32767);
    l_columns      VARCHAR2(32767);
    l_count        NUMBER;
    l_warnings     NUMBER:=0;
    l_orphans      NUMBER:=0;
    l_duplicates   NUMBER:=0;
    l_signature    VARCHAR2(200);
    l_previous     VARCHAR2(200);
    l_status       VARCHAR2(10);
    l_module       VARCHAR2(20);
  BEGIN
    DELETE FROM prod.dct_integrity_result;

    FOR fk IN (
      SELECT constraint_name,table_name,r_constraint_name,r_owner,status,validated
        FROM user_constraints
       WHERE constraint_type='R'
         AND (status<>'ENABLED' OR validated<>'VALIDATED')
       ORDER BY table_name,constraint_name
    ) LOOP
      l_join:=''; l_nonnull:='';
      FOR c IN (
        SELECT cc.column_name child_column,pc.column_name parent_column,
               pk.table_name parent_table,cc.position
          FROM user_cons_columns cc
          JOIN user_constraints child_fk ON child_fk.constraint_name=cc.constraint_name
          JOIN all_constraints pk ON pk.owner=child_fk.r_owner
                                 AND pk.constraint_name=child_fk.r_constraint_name
          JOIN all_cons_columns pc ON pc.owner=pk.owner AND pc.constraint_name=pk.constraint_name
                                  AND pc.position=cc.position
         WHERE cc.constraint_name=fk.constraint_name
         ORDER BY cc.position
      ) LOOP
        IF l_join IS NULL OR l_join='' THEN
          l_join:='p.'||DBMS_ASSERT.SIMPLE_SQL_NAME(c.parent_column)||'=c.'||
                  DBMS_ASSERT.SIMPLE_SQL_NAME(c.child_column);
          l_nonnull:='c.'||DBMS_ASSERT.SIMPLE_SQL_NAME(c.child_column)||' IS NOT NULL';
        ELSE
          l_join:=l_join||' AND p.'||DBMS_ASSERT.SIMPLE_SQL_NAME(c.parent_column)||'=c.'||
                  DBMS_ASSERT.SIMPLE_SQL_NAME(c.child_column);
          l_nonnull:=l_nonnull||' AND c.'||DBMS_ASSERT.SIMPLE_SQL_NAME(c.child_column)||' IS NOT NULL';
        END IF;
      END LOOP;

      SELECT table_name INTO l_columns FROM all_constraints
       WHERE owner=fk.r_owner AND constraint_name=fk.r_constraint_name;
      l_sql:='SELECT COUNT(*) FROM PROD.'||
             DBMS_ASSERT.SIMPLE_SQL_NAME(fk.table_name)||' c WHERE '||l_nonnull||
             ' AND NOT EXISTS (SELECT 1 FROM '||DBMS_ASSERT.SCHEMA_NAME(fk.r_owner)||'.'||
             DBMS_ASSERT.SIMPLE_SQL_NAME(l_columns)||' p WHERE '||l_join||')';
      EXECUTE IMMEDIATE l_sql INTO l_count;

      l_warnings:=l_warnings+1;
      l_orphans:=l_orphans+l_count;
      l_module:=module_for_table(fk.table_name);
      INSERT INTO prod.dct_integrity_result
        (rule_type,module_code,table_name,constraint_name,issue_count,details)
      VALUES
        (CASE WHEN l_count>0 THEN 'ORPHAN_ROWS' ELSE 'FK_NOT_VALIDATED' END,
         l_module,fk.table_name,fk.constraint_name,l_count,
         CASE WHEN l_count>0 THEN 'Foreign-key check found child rows without a parent.'
              WHEN fk.status<>'ENABLED' THEN 'Foreign key is disabled; no orphan rows were found.'
              ELSE 'Foreign key is enabled but older rows were not validated; no orphan rows were found.' END);
    END LOOP;

    FOR uq IN (
      SELECT constraint_name,table_name,status,validated
        FROM user_constraints
       WHERE constraint_type IN ('P','U')
         AND (status<>'ENABLED' OR validated<>'VALIDATED')
       ORDER BY table_name,constraint_name
    ) LOOP
      SELECT LISTAGG(DBMS_ASSERT.SIMPLE_SQL_NAME(column_name),',') WITHIN GROUP (ORDER BY position)
        INTO l_columns FROM user_cons_columns WHERE constraint_name=uq.constraint_name;
      l_sql:='SELECT COUNT(*) FROM (SELECT '||l_columns||' FROM '||
             'PROD.'||DBMS_ASSERT.SIMPLE_SQL_NAME(uq.table_name)||
             ' GROUP BY '||l_columns||' HAVING COUNT(*)>1)';
      EXECUTE IMMEDIATE l_sql INTO l_count;
      l_warnings:=l_warnings+1;
      l_duplicates:=l_duplicates+l_count;
      l_module:=module_for_table(uq.table_name);
      INSERT INTO prod.dct_integrity_result
        (rule_type,module_code,table_name,constraint_name,issue_count,details)
      VALUES
        (CASE WHEN l_count>0 THEN 'DUPLICATE_GROUPS' ELSE 'UNVALIDATED_UNIQUE' END,
         l_module,uq.table_name,uq.constraint_name,l_count,
         CASE WHEN l_count>0 THEN 'Duplicate key groups were found.'
              ELSE 'Unique constraint is disabled or not validated; no duplicates were found.' END);
    END LOOP;

    SELECT l_warnings||':'||l_orphans||':'||l_duplicates||':'||
           NVL(SUM(ORA_HASH(rule_type||':'||table_name||':'||constraint_name||':'||issue_count)),0)
      INTO l_signature FROM prod.dct_integrity_result;
    l_status:=CASE WHEN l_warnings=0 THEN 'HEALTHY' ELSE 'WARNING' END;
    BEGIN
      SELECT alerted_signature INTO l_previous FROM prod.dct_integrity_state WHERE state_id=1;
    EXCEPTION WHEN NO_DATA_FOUND THEN l_previous:=NULL;
    END;

    IF l_status='WARNING' AND NVL(l_previous,'-')<>l_signature THEN
      FOR u IN (
        SELECT DISTINCT usr.user_id
          FROM prod.dct_users usr
          JOIN prod.dct_user_roles ur ON ur.user_id=usr.user_id
          JOIN prod.dct_roles r ON r.role_id=ur.role_id
         WHERE r.role_code='SYS_ADMIN' AND r.is_active='Y' AND usr.is_active='Y' AND ur.is_active='Y'
           AND TRUNC(SYSDATE)>=TRUNC(ur.start_date)
           AND (ur.end_date IS NULL OR TRUNC(SYSDATE)<=TRUNC(ur.end_date))
      ) LOOP
        prod.dct_notify.send(u.user_id,'WARNING','Database integrity warning',
          l_warnings||' constraint(s) need validation. Actual data findings: '||
          l_orphans||' orphan row(s), '||l_duplicates||' duplicate group(s).',
          NULL,NULL,'ADMIN','/Admin/Jet/index.html#systemSettings');
      END LOOP;
    END IF;

    MERGE INTO prod.dct_integrity_state s
    USING (SELECT 1 state_id FROM dual) x ON (s.state_id=x.state_id)
    WHEN MATCHED THEN UPDATE SET
      s.checked_at=SYSTIMESTAMP,s.checked_by=SUBSTR(NVL(p_checked_by,'SYSTEM'),1,100),
      s.integrity_status=l_status,s.constraint_warnings=l_warnings,s.orphan_rows=l_orphans,
      s.duplicate_groups=l_duplicates,s.issue_signature=l_signature,
      s.alerted_signature=CASE WHEN l_status='WARNING' THEN l_signature ELSE NULL END,
      s.last_alert_at=CASE WHEN l_status='WARNING' AND NVL(l_previous,'-')<>l_signature
                           THEN SYSTIMESTAMP ELSE s.last_alert_at END
    WHEN NOT MATCHED THEN INSERT
      (state_id,checked_at,checked_by,integrity_status,constraint_warnings,orphan_rows,
       duplicate_groups,issue_signature,alerted_signature,last_alert_at)
    VALUES
      (1,SYSTIMESTAMP,SUBSTR(NVL(p_checked_by,'SYSTEM'),1,100),l_status,l_warnings,l_orphans,
       l_duplicates,l_signature,CASE WHEN l_status='WARNING' THEN l_signature END,
       CASE WHEN l_status='WARNING' THEN SYSTIMESTAMP END);
    COMMIT;
  EXCEPTION WHEN OTHERS THEN
    ROLLBACK;
    RAISE;
  END perform_check;

  PROCEDURE check_integrity IS BEGIN perform_check('SCHEDULER'); END;
  PROCEDURE run_integrity_check(p_checked_by VARCHAR2) IS BEGIN perform_check(p_checked_by); END;
END dct_integrity_pkg;
