CREATE OR REPLACE PROCEDURE admin.setup_dct_integrity_ords_tmp AS
BEGIN
  ORDS.DEFINE_TEMPLATE(p_module_name=>'dct.admin',p_pattern=>'maintenance/data-integrity');
  ORDS.DEFINE_HANDLER(
    p_module_name=>'dct.admin',p_pattern=>'maintenance/data-integrity',p_method=>'GET',
    p_source_type=>ORDS.source_type_plsql,
    p_source=>q'!
DECLARE
  l_user VARCHAR2(100):=dct_rest.validate_session;
  l_state prod.dct_integrity_state%ROWTYPE;
BEGIN
  IF l_user IS NULL THEN dct_rest.err(401,'Unauthorized'); RETURN; END IF;
  IF NOT dct_auth.has_role(l_user,'SYS_ADMIN') THEN
    dct_rest.err(403,'Only SYS_ADMIN may view data integrity results'); RETURN;
  END IF;
  SELECT * INTO l_state FROM prod.dct_integrity_state WHERE state_id=1;
  dct_rest.json_header; APEX_JSON.initialize_output; APEX_JSON.open_object;
  APEX_JSON.write('status',l_state.integrity_status);
  APEX_JSON.write('constraintWarnings',l_state.constraint_warnings);
  APEX_JSON.write('orphanRows',l_state.orphan_rows);
  APEX_JSON.write('duplicateGroups',l_state.duplicate_groups);
  APEX_JSON.write('checkedAt',TO_CHAR(l_state.checked_at AT TIME ZONE 'Asia/Dubai','YYYY-MM-DD HH24:MI'));
  APEX_JSON.write('checkedBy',l_state.checked_by);
  APEX_JSON.open_array('issues');
  FOR r IN (SELECT rule_type,module_code,table_name,constraint_name,issue_count,details
              FROM prod.dct_integrity_result ORDER BY issue_count DESC,module_code,table_name) LOOP
    APEX_JSON.open_object;
    APEX_JSON.write('ruleType',r.rule_type); APEX_JSON.write('moduleCode',r.module_code);
    APEX_JSON.write('tableName',r.table_name); APEX_JSON.write('constraintName',r.constraint_name);
    APEX_JSON.write('issueCount',r.issue_count); APEX_JSON.write('details',r.details);
    APEX_JSON.close_object;
  END LOOP;
  APEX_JSON.close_array; APEX_JSON.close_object;
EXCEPTION WHEN NO_DATA_FOUND THEN dct_rest.err(404,'Data integrity has not been checked yet');
WHEN OTHERS THEN dct_rest.err(500,SQLERRM);
END;
!');

  ORDS.DEFINE_HANDLER(
    p_module_name=>'dct.admin',p_pattern=>'maintenance/data-integrity',p_method=>'POST',
    p_source_type=>ORDS.source_type_plsql,
    p_source=>q'!
DECLARE
  l_user VARCHAR2(100):=dct_rest.validate_session;
  l_state prod.dct_integrity_state%ROWTYPE;
BEGIN
  IF l_user IS NULL THEN dct_rest.err(401,'Unauthorized'); RETURN; END IF;
  IF NOT dct_auth.has_role(l_user,'SYS_ADMIN') THEN
    dct_rest.err(403,'Only SYS_ADMIN may run data integrity checks'); RETURN;
  END IF;
  prod.dct_integrity_pkg.run_integrity_check(l_user);
  SELECT * INTO l_state FROM prod.dct_integrity_state WHERE state_id=1;
  dct_rest.json_header; APEX_JSON.initialize_output; APEX_JSON.open_object;
  APEX_JSON.write('status',l_state.integrity_status);
  APEX_JSON.write('constraintWarnings',l_state.constraint_warnings);
  APEX_JSON.write('orphanRows',l_state.orphan_rows);
  APEX_JSON.write('duplicateGroups',l_state.duplicate_groups);
  APEX_JSON.write('checkedAt',TO_CHAR(l_state.checked_at AT TIME ZONE 'Asia/Dubai','YYYY-MM-DD HH24:MI'));
  APEX_JSON.close_object;
EXCEPTION WHEN OTHERS THEN dct_rest.err(500,SQLERRM);
END;
!');
END;
/

BEGIN admin.setup_dct_integrity_ords_tmp; COMMIT; END;
