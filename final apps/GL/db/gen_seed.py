# -*- coding: utf-8 -*-
"""
Generate 06_gl_seed.sql from the three source CSVs.
  - class types (SECTOR / CHAPTER / DCT_PROGRAM)
  - class values (sectors, chapters, PBB program hierarchy)
  - date-tracked mappings: Sector (cost_center) + Chapter (appropriation) explicit;
    DCT_PROGRAM matched by description in SQL for program codes used in combinations.
Re-runnable: seed mappings are tagged created_by='SEED' and cleared first.
"""
import csv, os, re

HERE = os.path.dirname(os.path.abspath(__file__))
SRC  = os.path.join(HERE, 'source')
OUT  = os.path.join(HERE, '06_gl_seed.sql')
BASELINE = "DATE '2024-01-01'"

def q(s):
    return "'" + (s or '').replace("'", "''") + "'"

def slug(s):
    s = re.sub(r'[^A-Za-z0-9]+', '_', (s or '').strip()).strip('_').upper()
    return s[:60] or 'NA'

# ---------- Sector (Cost Centers List) ----------
sectors = {}          # value_code -> name
cc_to_sector = {}     # cost_center(7) -> value_code
with open(os.path.join(SRC, 'Cost Centers List 2026.csv'), encoding='utf-8') as f:
    rows = list(csv.reader(f))
hdr = next(i for i, r in enumerate(rows) if r and r[0] == 'CostCenter_2018_2019')
for r in rows[hdr+1:]:
    if len(r) < 5:
        continue
    cc, sec = r[2].strip(), r[4].strip()
    if not cc.isdigit() or not sec or sec == '-':
        continue
    code = slug(sec)
    sectors[code] = sec.strip()
    cc_to_sector[cc.zfill(7)] = code

# ---------- Chapter (Future 2 & Chapter Mapping) ----------
CH_TAG = {'CH1':'Payroll','CH2':'Opex','CH3':'Capex','CH4':'Subsidy',
          'CH5':'Aids & Grants','CH6':'CWIP','STIMULUS':'Stimulus'}
CH_NAME = {'CH1':'Chapter 1','CH2':'Chapter 2','CH3':'Chapter 3','CH4':'Chapter 4',
           'CH5':'Chapter 5','CH6':'Chapter 6','STIMULUS':'Stimulus'}
def chapter_code(txt):
    t = (txt or '').strip().lower()
    if t == 'stimulus':
        return 'STIMULUS'
    m = re.search(r'chapter\s*(\d)', t)
    return 'CH' + m.group(1) if m else None
appr_to_chapter = {}   # appropriation(6) -> chapter code
with open(os.path.join(SRC, 'Future 2 & Chapter Mapping.csv'), encoding='utf-8') as f:
    for r in list(csv.reader(f))[1:]:
        if len(r) < 3 or not r[0].strip().isdigit():
            continue
        ch = chapter_code(r[2])
        if ch:
            appr_to_chapter[r[0].strip().zfill(6)] = ch

# ---------- DCT Program (PBB) hierarchy ----------
programs = []   # (value_code, name, parent_code or None)
last_parent = None
with open(os.path.join(SRC, 'Updated PBB.csv'), encoding='utf-8') as f:
    for r in csv.reader(f):
        if len(r) < 3:
            continue
        parent, child, name = r[0].strip(), r[1].strip(), r[2].strip()
        if parent and not child:
            programs.append((parent, name, None)); last_parent = parent
        elif child:
            programs.append((child, name, last_parent))

# ---------- emit ----------
L = []
w = L.append
w("-- ===========================================================================")
w("-- General Ledger (App 210) - Layer 6 - seed (GENERATED by gen_seed.py)")
w("-- Sectors + cost-center mappings, Chapters + appropriation mappings,")
w("-- PBB DCT-Program hierarchy + description-matched program mappings.")
w("-- Re-runnable: seed mappings tagged created_by='SEED' and cleared first.")
w("-- ===========================================================================")
w("SET DEFINE OFF")
w("SET SQLBLANKLINES ON")
w("ALTER SESSION DISABLE PARALLEL DML;")
w("")

# class types
w("-- ---- dimension catalog ----")
types = [
    ('SECTOR','Sector','القطاع','COST_CENTER',7,None,'N',10),
    ('CHAPTER','Chapter','الباب','APPROPRIATION',6,None,'N',20),
    ('DCT_PROGRAM','DCT Program','البرنامج','PROGRAM_CODE',6,None,'Y',30),
]
for code,en,ar,seg,wd,pfx,hier,ordr in types:
    w("MERGE INTO prod.dct_gl_class_type t USING (SELECT %s code FROM dual) s" % q(code))
    w("  ON (t.class_type_code = s.code)")
    w("WHEN NOT MATCHED THEN INSERT (class_type_code,name_en,name_ar,segment_key,seg_pad_width,seg_prefix,is_hierarchical,display_order,is_active,created_by)")
    w("  VALUES (%s,%s,%s,%s,%d,%s,%s,%d,'Y','SEED')" % (
        q(code),q(en),q(ar),q(seg),wd,(q(pfx) if pfx else 'NULL'),q(hier),ordr))
    w("WHEN MATCHED THEN UPDATE SET t.name_en=%s,t.name_ar=%s,t.segment_key=%s,t.seg_pad_width=%d,t.is_hierarchical=%s,t.display_order=%d;" % (
        q(en),q(ar),q(seg),wd,q(hier),ordr))
    w("")

def merge_value(ct, vc, nm, parent=None, tag=None, ordr=0):
    parent_expr = ("(SELECT class_value_id FROM prod.dct_gl_class_value WHERE class_type_code=%s AND value_code=%s)"
                   % (q(ct), q(parent))) if parent else 'NULL'
    w("MERGE INTO prod.dct_gl_class_value t USING (SELECT %s ct,%s vc FROM dual) s" % (q(ct), q(vc)))
    w("  ON (t.class_type_code = s.ct AND t.value_code = s.vc)")
    w("WHEN NOT MATCHED THEN INSERT (class_type_code,value_code,name_en,parent_value_id,tag,display_order,is_active,created_by)")
    w("  VALUES (%s,%s,%s,%s,%s,%d,'Y','SEED')" % (q(ct),q(vc),q(nm),parent_expr,(q(tag) if tag else 'NULL'),ordr))
    w("WHEN MATCHED THEN UPDATE SET t.name_en=%s,t.parent_value_id=%s,t.tag=%s,t.display_order=%d;" % (
        q(nm),parent_expr,(q(tag) if tag else 'NULL'),ordr))

w("-- ---- Sector values ----")
for i,(code,name) in enumerate(sorted(sectors.items())):
    merge_value('SECTOR', code, name, ordr=(i+1)*10)
w("")
w("-- ---- Chapter values ----")
for i,code in enumerate(['CH1','CH2','CH3','CH4','CH5','CH6','STIMULUS']):
    merge_value('CHAPTER', code, CH_NAME[code], tag=CH_TAG[code], ordr=(i+1)*10)
w("")
w("-- ---- DCT Program values (parents first, then children) ----")
order = 0
for code,name,parent in [p for p in programs if p[2] is None] + [p for p in programs if p[2] is not None]:
    order += 10
    merge_value('DCT_PROGRAM', code, name, parent=parent, ordr=order)
w("")

# mappings
w("-- ---- clear prior seed mappings (re-runnable) ----")
w("DELETE FROM prod.dct_gl_seg_class_map WHERE created_by='SEED';")
w("")
w("-- ---- Sector mappings (cost_center -> sector) ----")
for cc,code in sorted(cc_to_sector.items()):
    w(("INSERT INTO prod.dct_gl_seg_class_map (class_type_code,segment_value,class_value_id,start_date,end_date,created_by) "
       "SELECT 'SECTOR',%s,class_value_id,%s,NULL,'SEED' FROM prod.dct_gl_class_value "
       "WHERE class_type_code='SECTOR' AND value_code=%s;") % (q(cc), BASELINE, q(code)))
w("")
w("-- ---- Chapter mappings (appropriation -> chapter) ----")
for appr,code in sorted(appr_to_chapter.items()):
    w(("INSERT INTO prod.dct_gl_seg_class_map (class_type_code,segment_value,class_value_id,start_date,end_date,created_by) "
       "SELECT 'CHAPTER',%s,class_value_id,%s,NULL,'SEED' FROM prod.dct_gl_class_value "
       "WHERE class_type_code='CHAPTER' AND value_code=%s;") % (q(appr), BASELINE, q(code)))
w("")
w("-- ---- DCT Program mappings (program_code used in combinations, matched to PBB by description) ----")
w("INSERT INTO prod.dct_gl_seg_class_map (class_type_code,segment_value,class_value_id,start_date,end_date,created_by)")
w("SELECT 'DCT_PROGRAM', prod.dct_gl_class_pkg.norm(p.program_code,6), cv.class_value_id, %s, NULL, 'SEED'" % BASELINE)
w("FROM (SELECT DISTINCT program_code FROM prod.gl_src_combinations WHERE program_code IS NOT NULL) p")
w("JOIN prod.gl_src_program gp ON gp.program_code = p.program_code")
w("JOIN prod.dct_gl_class_value cv ON cv.class_type_code='DCT_PROGRAM'")
w("  AND UPPER(TRIM(cv.name_en)) = UPPER(TRIM(gp.program_description))")
w("WHERE NOT EXISTS (SELECT 1 FROM prod.dct_gl_seg_class_map m")
w("  WHERE m.class_type_code='DCT_PROGRAM' AND m.segment_value=prod.dct_gl_class_pkg.norm(p.program_code,6));")
w("")
w("COMMIT;")
w("PROMPT GL seed loaded (sectors=%d, chapters=7, programs=%d).""" % (len(sectors), len(programs)))
w("")

with open(OUT, 'w', encoding='utf-8', newline='\r\n') as f:
    f.write('\n'.join(L))
print('wrote', OUT)
print('sectors=%d  cc_maps=%d  appr_maps=%d  programs=%d' % (
    len(sectors), len(cc_to_sector), len(appr_to_chapter), len(programs)))
