-- -----------------------------------------------------------------------------
-- 3. ORDS: voucher create stamps the invoice number; voucher detail carries the
--    period, the full budget picture and its attachments
-- -----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE setup_fl_voucher_enh_tmp AS
    c_mod CONSTANT VARCHAR2(30) := 'fl.rest';

    PROCEDURE deft(p_pattern VARCHAR2) IS
    BEGIN
        ORDS.DEFINE_TEMPLATE(p_module_name => c_mod,
            p_pattern => REPLACE(p_pattern, '[COLON]', CHR(58)));
    EXCEPTION WHEN OTHERS THEN NULL;
    END;

    PROCEDURE defh(p_pattern VARCHAR2, p_method VARCHAR2, p_source CLOB) IS
    BEGIN
        ORDS.DEFINE_HANDLER(p_module_name => c_mod,
            p_pattern => REPLACE(p_pattern, '[COLON]', CHR(58)),
            p_method => p_method, p_source_type => ORDS.source_type_plsql,
            p_source => REPLACE(p_source, '[COLON]', CHR(58)));
    END;
BEGIN
    -- ---------- create: schedule stamp (35) + auto invoice number -----------
    defh('vouchers/', 'POST', q'!
DECLARE
  l_user   VARCHAR2(100) := dct_rest.validate_session;
  l_id     NUMBER;
  l_num    VARCHAR2(50);
  l_sched  NUMBER;
  l_s      dct_fl_payment_schedule%ROWTYPE;
  l_con    dct_fl_contracts%ROWTYPE;
  l_open   NUMBER;
  l_desc   VARCHAR2(1000);
  l_inv    VARCHAR2(100);
  l_new    CLOB;
BEGIN
  IF l_user IS NULL THEN dct_rest.err(401,'Unauthorized'); RETURN; END IF;
  dct_rest.parse_body([COLON]body);
  l_sched := APEX_JSON.get_number(p_path => 'scheduleId');
  IF l_sched IS NULL THEN
    dct_rest.err(400,'scheduleId is required (vouchers are generated from payment schedule rows)'); RETURN;
  END IF;
  SELECT * INTO l_s FROM dct_fl_payment_schedule WHERE schedule_id = l_sched;
  IF l_s.status != 'PENDING' THEN
    dct_rest.err(400,'Schedule row is not PENDING (status: ' || l_s.status || ')'); RETURN;
  END IF;
  SELECT COUNT(*) INTO l_open FROM dct_fl_payment_vouchers
  WHERE schedule_id = l_sched AND status NOT IN ('REJECTED','CANCELLED');
  IF l_open > 0 THEN
    dct_rest.err(400,'A voucher already exists for this payment period'); RETURN;
  END IF;
  SELECT * INTO l_con FROM dct_fl_contracts WHERE contract_id = l_s.contract_id;
  l_num  := 'FL-VCH-' || TO_CHAR(seq_fl_voucher_number.NEXTVAL, 'FM000000');
  l_desc := NVL(dct_fl_pkg.get_setting('VOUCHER_DEFAULT_DESCRIPTION'),
                'Freelancer Payment - {CONTRACT_NUMBER} - {PERIOD_LABEL}');
  l_desc := REPLACE(REPLACE(l_desc, '{CONTRACT_NUMBER}', l_con.contract_number),
                    '{PERIOD_LABEL}', l_s.period_label);
  -- system-generated invoice number (module setting); a value in the body wins
  l_inv  := NVL(APEX_JSON.get_varchar2(p_path => 'invoiceNumber'),
                dct_fl_invoice_number(l_sched, l_num));
  INSERT INTO dct_fl_payment_vouchers (
    voucher_number, contract_id, freelancer_id, schedule_id,
    period_label, due_date, amount,
    invoice_number, invoice_date,
    payment_method, pay_group, description,
    coding_type, cc_id_gl, project_number, task_number, expenditure_type,
    post_to_fusion, status, payment_status, created_by, updated_by
  ) VALUES (
    l_num, l_con.contract_id, l_con.freelancer_id, l_sched,
    l_s.period_label, l_s.due_date, l_s.amount,
    l_inv,
    NVL(TO_DATE(APEX_JSON.get_varchar2(p_path => 'invoiceDate'), 'YYYY-MM-DD'), TRUNC(SYSDATE)),
    NVL(APEX_JSON.get_varchar2(p_path => 'paymentMethod'),
        NVL(dct_fl_pkg.get_setting('DEFAULT_PAYMENT_METHOD'), 'BANK_TRANSFER')),
    NVL(APEX_JSON.get_varchar2(p_path => 'payGroup'),
        NVL(dct_fl_pkg.get_setting('DEFAULT_PAY_GROUP'), 'FREELANCER')),
    l_desc,
    l_con.coding_type, l_con.cc_id_gl, l_con.project_number, l_con.task_number, l_con.expenditure_type,
    'N', 'DRAFT', 'PENDING', l_user, l_user
  ) RETURNING voucher_id INTO l_id;
  UPDATE dct_fl_payment_schedule
  SET    status = 'VOUCHER_GENERATED', voucher_id = l_id, updated_at = SYSTIMESTAMP
  WHERE  schedule_id = l_sched;
  COMMIT;
  l_new := dct_audit_pkg.snap('DCT_FL_PAYMENT_VOUCHERS','voucher_id', TO_CHAR(l_id));
  dct_audit_pkg.log(l_user,'CREATE','DCT_FL_PAYMENT_VOUCHERS', TO_CHAR(l_id), 'FL',
                    p_object_ref=>l_num, p_new=>l_new);
  OWA_UTIL.status_line(201, NULL, FALSE);
  dct_rest.json_header;
  APEX_JSON.initialize_output;
  APEX_JSON.open_object;
  APEX_JSON.write('voucherId', l_id);
  APEX_JSON.write('voucherNumber', l_num);
  APEX_JSON.write('invoiceNumber', NVL(l_inv, ''));
  APEX_JSON.write('scheduleStatus', 'VOUCHER_GENERATED');
  APEX_JSON.write('ok', TRUE);
  APEX_JSON.close_object;
EXCEPTION WHEN OTHERS THEN ROLLBACK; dct_rest.err(500, SQLERRM);
END;
!');

    -- ---------- detail: + payment period, full budget, contract position -----
    defh('vouchers/[COLON]id', 'GET', q'!
DECLARE
  l_user  VARCHAR2(100) := dct_rest.validate_session;
  l_found BOOLEAN := FALSE;
BEGIN
  IF l_user IS NULL THEN dct_rest.err(401,'Unauthorized'); RETURN; END IF;
  dct_rest.json_header;
  APEX_JSON.initialize_output;
  APEX_JSON.open_object;
  FOR r IN (
    SELECT v.*,
           c.billing_method, c.total_amount AS contract_amount,
           c.start_date AS contract_start, c.end_date AS contract_end,
           -- payment period the voucher pays for
           CASE UPPER(NVL(c.billing_method,'MONTHLY'))
             WHEN 'MONTHLY' THEN TRUNC(v.due_date, 'MM')
             WHEN 'WEEKLY'  THEN v.due_date - 6
             ELSE v.due_date
           END AS period_start,
           CASE UPPER(NVL(c.billing_method,'MONTHLY'))
             WHEN 'MONTHLY' THEN LAST_DAY(v.due_date)
             ELSE v.due_date
           END AS period_end,
           (SELECT NVL(SUM(s2.amount),0) FROM dct_fl_payment_schedule s2
            WHERE s2.contract_id = v.contract_id AND s2.status = 'PAID') AS contract_paid,
           (SELECT cc_string FROM dct_gl_coa_v g WHERE g.cc_id = v.cc_id_gl) AS gl_combination,
           (SELECT MAX(b.project_name) FROM dct_budget_utilization_v b
            WHERE b.project_number = v.project_number) AS project_name
    FROM   dct_fl_voucher_v      v
    JOIN   dct_fl_contracts      c ON c.contract_id = v.contract_id
    WHERE  v.voucher_id = [COLON]id
  ) LOOP
    l_found := TRUE;
    APEX_JSON.write('voucherId',      r.voucher_id);
    APEX_JSON.write('voucherNumber',  r.voucher_number);
    APEX_JSON.write('contractId',     r.contract_id);
    APEX_JSON.write('contractNumber', r.contract_number);
    APEX_JSON.write('freelancerId',   r.freelancer_id);
    APEX_JSON.write('freelancerName', r.freelancer_name);
    APEX_JSON.write('vendorNumber',   NVL(r.vendor_number, ''));
    APEX_JSON.write('scheduleId',     r.schedule_id);
    APEX_JSON.write('periodLabel',    NVL(r.period_label, ''));
    APEX_JSON.write('periodStart',    TO_CHAR(r.period_start, 'YYYY-MM-DD'));
    APEX_JSON.write('periodEnd',      TO_CHAR(r.period_end,   'YYYY-MM-DD'));
    APEX_JSON.write('billingMethod',  NVL(r.billing_method, ''));
    APEX_JSON.write('dueDate',        TO_CHAR(r.due_date, 'YYYY-MM-DD'));
    APEX_JSON.write('amount',         r.amount);
    APEX_JSON.write('invoiceNumber',  NVL(r.invoice_number, ''));
    APEX_JSON.write('invoiceDate',    TO_CHAR(r.invoice_date, 'YYYY-MM-DD'));
    APEX_JSON.write('paymentMethod',  r.payment_method);
    APEX_JSON.write('payGroup',       r.pay_group);
    APEX_JSON.write('description',    NVL(r.description, ''));
    APEX_JSON.write('codingType',     r.coding_type);
    APEX_JSON.write('ccIdGl',         r.cc_id_gl);
    APEX_JSON.write('glCombination',  NVL(r.gl_combination, ''));
    APEX_JSON.write('projectNumber',  NVL(r.project_number, ''));
    APEX_JSON.write('projectName',    NVL(r.project_name, ''));
    APEX_JSON.write('taskNumber',     NVL(r.task_number, ''));
    APEX_JSON.write('expenditureType',NVL(r.expenditure_type, ''));
    APEX_JSON.write('contractAmount', NVL(r.contract_amount, 0));
    APEX_JSON.write('contractPaid',   NVL(r.contract_paid, 0));
    APEX_JSON.write('contractStart',  TO_CHAR(r.contract_start, 'YYYY-MM-DD'));
    APEX_JSON.write('contractEnd',    TO_CHAR(r.contract_end, 'YYYY-MM-DD'));
    APEX_JSON.write('postToFusion',   r.post_to_fusion);
    APEX_JSON.write('status',         r.status);
    APEX_JSON.write('paymentStatus',  r.payment_status);
    APEX_JSON.write('paymentDate',    TO_CHAR(r.payment_date, 'YYYY-MM-DD'));
    APEX_JSON.write('paymentReference', NVL(r.payment_reference, ''));
    APEX_JSON.write('approvalInstanceId', r.approval_instance_id);
    APEX_JSON.write('notes',          NVL(r.notes, ''));
    APEX_JSON.write('createdBy',      r.created_by);
    APEX_JSON.write('createdAt',      TO_CHAR(dct_to_local(r.created_at), 'DD-Mon-YYYY HH:MI AM'));
    APEX_JSON.write('updatedAt',      TO_CHAR(dct_to_local(r.updated_at), 'DD-Mon-YYYY HH:MI AM'));

    -- full budget picture for the line this voucher is charged to
    APEX_JSON.open_object('budget');
    APEX_JSON.write('year', TO_NUMBER(TO_CHAR(r.due_date, 'YYYY')));
    IF r.coding_type = 'PROJECT' AND r.project_number IS NOT NULL THEN
      FOR b IN (
        SELECT NVL(SUM(budget),0) bud, NVL(SUM(actual_ap),0) ap, NVL(SUM(actual_grn),0) grn,
               NVL(SUM(commitment_pr),0) pr, NVL(SUM(obligation_po),0) po,
               NVL(SUM(fund_available),0) fund, COUNT(*) rows_found
        FROM   dct_budget_utilization_v
        WHERE  budget_year = TO_NUMBER(TO_CHAR(r.due_date, 'YYYY'))
        AND    project_number = r.project_number
        AND    (r.task_number      IS NULL OR task_number      = r.task_number)
        AND    (r.expenditure_type IS NULL OR expenditure_type = r.expenditure_type)
      ) LOOP
        APEX_JSON.write('found',         CASE WHEN b.rows_found > 0 THEN 'Y' ELSE 'N' END);
        APEX_JSON.write('budget',        b.bud);
        APEX_JSON.write('actual',        b.ap + b.grn);
        APEX_JSON.write('commitment',    b.pr);
        APEX_JSON.write('obligation',    b.po);
        APEX_JSON.write('fundAvailable', b.fund);
        APEX_JSON.write('sufficient',
          CASE WHEN b.rows_found > 0 AND b.fund >= NVL(r.amount,0) THEN 'Y' ELSE 'N' END);
      END LOOP;
    ELSE
      APEX_JSON.write('found', 'N');
    END IF;
    APEX_JSON.close_object;
  END LOOP;
  IF NOT l_found THEN
    APEX_JSON.free_output;
    dct_rest.err(404,'Voucher not found'); RETURN;
  END IF;
  APEX_JSON.write('ok', TRUE);
  APEX_JSON.close_object;
EXCEPTION WHEN OTHERS THEN dct_rest.err(500, SQLERRM);
END;
!');

    -- ---------- attachments on a voucher ------------------------------------
    deft('vouchers/[COLON]id/documents');
    defh('vouchers/[COLON]id/documents', 'GET', q'!
DECLARE
  l_user VARCHAR2(100) := dct_rest.validate_session;
BEGIN
  IF l_user IS NULL THEN dct_rest.err(401,'Unauthorized'); RETURN; END IF;
  dct_rest.json_header;
  APEX_JSON.initialize_output;
  APEX_JSON.open_object;
  APEX_JSON.open_array('items');
  FOR r IN (
    SELECT d.doc_id, d.doc_type_id, dt.doc_type_name_en, d.file_name, d.mime_type,
           NVL(d.file_size_bytes, 0) AS file_size, d.notes,
           TO_CHAR(dct_to_local(d.created_at), 'YYYY-MM-DD HH:MI AM') AS uploaded_at,
           u.display_name AS uploaded_by
    FROM   dct_documents      d
    LEFT JOIN dct_document_types dt ON dt.doc_type_id = d.doc_type_id
    LEFT JOIN dct_users          u  ON u.user_id      = d.created_by
    WHERE  d.source_module = 'FL' AND d.source_type = 'VOUCHER'
    AND    d.source_id = [COLON]id
    AND    d.is_active = 'Y' AND d.status = 'ACTIVE'
    ORDER BY d.doc_id
  ) LOOP
    APEX_JSON.open_object;
    APEX_JSON.write('documentId',   r.doc_id);
    APEX_JSON.write('docTypeId',    r.doc_type_id);
    APEX_JSON.write('docTypeName',  NVL(r.doc_type_name_en, 'Attachment'));
    APEX_JSON.write('documentName', NVL(r.file_name, 'document'));
    APEX_JSON.write('mimeType',     NVL(r.mime_type, ''));
    APEX_JSON.write('fileSize',     r.file_size);
    APEX_JSON.write('hasFile',      CASE WHEN r.file_size > 0 THEN 'Y' ELSE 'N' END);
    APEX_JSON.write('notes',        NVL(r.notes, ''));
    APEX_JSON.write('uploadedAt',   NVL(r.uploaded_at, ''));
    APEX_JSON.write('uploadedBy',   NVL(r.uploaded_by, ''));
    APEX_JSON.close_object;
  END LOOP;
  APEX_JSON.close_array;
  APEX_JSON.close_object;
EXCEPTION WHEN OTHERS THEN dct_rest.err(500, SQLERRM);
END;
!');

END setup_fl_voucher_enh_tmp;
