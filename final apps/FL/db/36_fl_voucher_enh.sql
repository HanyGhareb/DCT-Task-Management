--           creates an ADMIN synonym. Additive + re-runnable. Re-run after any
--           08 re-run; run AFTER 35.)
-- Purpose : Voucher review round (2026-07-14):
--   1. INVOICE NUMBER is generated by the system from a module-setting template
--      (Module Settings -> Voucher):
--        INVOICE_NUMBER_AUTO      Y/N  (default Y)
--        INVOICE_NUMBER_TEMPLATE  default  FL-{FREELANCER_NUMBER}-{CONTRACT_NUMBER}-{PERIOD}
--      Placeholders: {FREELANCER_NUMBER} {FREELANCER_ID} {CONTRACT_NUMBER}
--                    {CONTRACT_ID} {PERIOD} {SCHEDULE_ID} {VOUCHER_NUMBER}
--                    {YYYY} {MM}. Built by prod.dct_fl_invoice_number().
--   2. PAYMENT PERIOD from/to -- derived from the contract's billing method
--      (MONTHLY = the due date's calendar month, WEEKLY = the 7 days ending on
--      the due date, otherwise the due date itself). No new columns.
--   3. FULL BUDGET DETAILS on the voucher: project + name, task, expenditure
--      type, GL combination, and the live budget / actual / commitment /
--      fund-available figures for that budget line and year
--      (DCT_BUDGET_UTILIZATION_V), plus the contract's own committed vs paid.
--   4. ATTACHMENTS on a voucher -- the unified DCT_DOCUMENTS store with
--      source_type = 'VOUCHER', source_id = voucher_id (reference_id still
--      carries the freelancer_id, per the FL convention). New
--      GET /fl/vouchers/:id/documents; create/upload/delete reuse the existing
--      /documents endpoints.
-- Source  : handlers synced into 08_fl_ords.sql.
-- =============================================================================

