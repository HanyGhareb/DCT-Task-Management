-- -----------------------------------------------------------------------------
-- 1. Module settings (module_code FREELANCERS -- what DCT_FL_PKG.get_setting reads)
-- -----------------------------------------------------------------------------
DECLARE
    v_mod NUMBER;
    v_cnt NUMBER;

    PROCEDURE ensure_setting(p_key   VARCHAR2, p_val VARCHAR2, p_type VARCHAR2,
                             p_label VARCHAR2, p_desc VARCHAR2,
                             p_allowed VARCHAR2 DEFAULT NULL) IS
        v_n NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_n FROM prod.dct_module_settings
        WHERE  module_id = v_mod AND setting_key = p_key;
        IF v_n = 0 THEN
            INSERT INTO prod.dct_module_settings
                   (module_id, setting_key, setting_value, setting_label,
                    setting_description, value_type, allowed_values, default_value,
                    effective_date, updated_by, updated_at)
            VALUES (v_mod, p_key, p_val, p_label, p_desc, p_type, p_allowed, p_val,
                    SYSDATE, 'SYSTEM', SYSTIMESTAMP);
        END IF;
    END;
BEGIN
    SELECT module_id INTO v_mod FROM prod.dct_modules WHERE module_code = 'FREELANCERS';

    ensure_setting('INVOICE_NUMBER_AUTO', 'Y', 'BOOLEAN',
        'Auto-generate the invoice number',
        'When Yes, a voucher''s invoice number is generated from INVOICE_NUMBER_TEMPLATE '
        || 'as soon as the voucher is created. When No, the user types it in.',
        'Y,N');

    ensure_setting('INVOICE_NUMBER_TEMPLATE',
        '{FREELANCER_NUMBER}-{CONTRACT_NUMBER}-{PERIOD}', 'TEXT',
        'Invoice number template',
        'Placeholders: {FREELANCER_NUMBER} {FREELANCER_ID} {CONTRACT_NUMBER} {CONTRACT_ID} '
        || '{PERIOD} {SCHEDULE_ID} {VOUCHER_NUMBER} {YYYY} {MM}');

    COMMIT;
    DBMS_OUTPUT.PUT_LINE('FL voucher settings ready (INVOICE_NUMBER_AUTO / _TEMPLATE)');
END;
/

-- -----------------------------------------------------------------------------
-- 2. Invoice-number builder
-- -----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION prod.dct_fl_invoice_number (
    p_schedule_id   IN NUMBER,
    p_voucher_number IN VARCHAR2 DEFAULT NULL
) RETURN VARCHAR2 AS
-- Expands the INVOICE_NUMBER_TEMPLATE module setting for one schedule row.
-- Returns NULL when INVOICE_NUMBER_AUTO is not Y, so callers can fall back to
-- whatever the user typed.
    v_auto VARCHAR2(10);
    v_tpl  VARCHAR2(500);
    v_out  VARCHAR2(200);
BEGIN
    v_auto := NVL(prod.dct_fl_pkg.get_setting('INVOICE_NUMBER_AUTO'), 'Y');
    IF UPPER(v_auto) != 'Y' THEN
        RETURN NULL;
    END IF;
    v_tpl := NVL(prod.dct_fl_pkg.get_setting('INVOICE_NUMBER_TEMPLATE'),
                 'FL-{FREELANCER_NUMBER}-{CONTRACT_NUMBER}-{PERIOD}');

    FOR r IN (
        SELECT s.schedule_id, s.period_label, s.due_date,
               c.contract_id, c.contract_number,
               f.freelancer_id, NVL(f.vendor_number, TO_CHAR(f.freelancer_id)) AS vendor_number
        FROM   prod.dct_fl_payment_schedule s
        JOIN   prod.dct_fl_contracts        c ON c.contract_id   = s.contract_id
        JOIN   prod.dct_fl_freelancers      f ON f.freelancer_id = c.freelancer_id
        WHERE  s.schedule_id = p_schedule_id
    ) LOOP
        v_out := v_tpl;
        v_out := REPLACE(v_out, '{FREELANCER_NUMBER}', r.vendor_number);
        v_out := REPLACE(v_out, '{FREELANCER_ID}',     TO_CHAR(r.freelancer_id));
        v_out := REPLACE(v_out, '{CONTRACT_NUMBER}',   r.contract_number);
        v_out := REPLACE(v_out, '{CONTRACT_ID}',       TO_CHAR(r.contract_id));
        -- the period label is display text ("Week 05 2026"); squeeze it into a
        -- reference-safe token
        v_out := REPLACE(v_out, '{PERIOD}',
                    REGEXP_REPLACE(UPPER(NVL(r.period_label, TO_CHAR(r.due_date,'YYYY-MM'))),
                                   '[^A-Z0-9]+', '-'));
        v_out := REPLACE(v_out, '{SCHEDULE_ID}',    TO_CHAR(r.schedule_id));
        v_out := REPLACE(v_out, '{VOUCHER_NUMBER}', NVL(p_voucher_number, ''));
        v_out := REPLACE(v_out, '{YYYY}',           TO_CHAR(r.due_date, 'YYYY'));
        v_out := REPLACE(v_out, '{MM}',             TO_CHAR(r.due_date, 'MM'));
    END LOOP;
    RETURN SUBSTR(v_out, 1, 100);
END;
