define(['knockout', 'services/flService', 'services/authService', 'services/docFile',
        'shared/formGuard', 'shared/docUpload'],
function (ko, flService, authService, docFile, formGuard, docUpload) {
  'use strict';

  function VoucherDetailViewModel() {
    var self = this;

    var id = Number(sessionStorage.getItem('flVoucherId') || 0);
    self.loading    = ko.observable(true);
    self.data       = ko.observable({});
    self.successMsg = ko.observable('');
    self.errorMsg   = ko.observable('');
    self.saving     = ko.observable(false);

    self.invoiceNumber = ko.observable('');
    self.invoiceDate   = ko.observable('');
    self.paymentMethod = ko.observable('');
    self.notes         = ko.observable('');

    // Admin-only: a generated voucher stays correctable until it is approved.
    self.amount      = ko.observable(null);
    self.dueDate     = ko.observable('');
    self.periodLabel = ko.observable('');
    self.description = ko.observable('');
    self.payGroup    = ko.observable('');

    // Payment methods + pay groups come from the managed lookups (FL_PAYMENT_METHOD,
    // FL_PAY_GROUP), so Module Settings drives the list, not this file.
    self.paymentMethods = ko.observableArray([]);
    self.payGroups      = ko.observableArray([]);

    // Attachments live in the unified document store (source_type = VOUCHER).
    self.attachments = ko.observableArray([]);
    self.docBusy     = ko.observable(false);
    self.docTypes    = ko.observableArray([]);

    // The invoice number is generated by the system from the template unless the
    // module setting INVOICE_NUMBER_AUTO is switched off (Module Settings).
    self.autoInvoice = ko.observable(true);

    var user = authService.getCurrentUser() || { roles: [] };
    self.isAdmin = (user.roles || []).indexOf('FL_ADMIN') >= 0 || (user.roles || []).indexOf('SYS_ADMIN') >= 0;

    function flash(m) { self.successMsg(m); setTimeout(function () { self.successMsg(''); }, 4000); }

    self.money = function (v) {
      return Number(v || 0).toLocaleString('en-US', { maximumFractionDigits: 0 });
    };
    self.billingLabel = function (m) {
      return { MONTHLY: 'Monthly billing', WEEKLY: 'Weekly billing',
               PER_COUNT: 'Per deliverable' }[m] || m;
    };
    self.fileSizeTxt = function (d) {
      var b = Number((d && d.fileSize) || 0);
      if (!b) return '—';
      if (b < 1024) return b + ' B';
      if (b < 1024 * 1024) return Math.round(b / 1024) + ' KB';
      return (b / 1024 / 1024).toFixed(1) + ' MB';
    };

    /* ── the budget line this voucher is charged to ───────────────────────── */
    self.budget     = ko.computed(function () { return self.data().budget || {}; });
    self.hasBudget  = ko.computed(function () { return self.budget().found === 'Y'; });
    self.budgetYear = ko.computed(function () { return self.budget().year || ''; });
    self.budgetNote = ko.computed(function () {
      return self.data().codingType === 'GL'
        ? 'GL-level coding — the project balances view does not apply to this voucher.'
        : 'No budget line found for this coding in ' + (self.budgetYear() || 'the budget year') + '.';
    });
    self.usedPct = ko.computed(function () {
      var b = self.budget();
      if (!b.budget) return 0;
      var used = (b.actual || 0) + (b.commitment || 0) + (b.obligation || 0);
      return Math.max(0, Math.min(100, Math.round(used / b.budget * 100)));
    });
    self.contractPct = ko.computed(function () {
      var d = self.data();
      if (!d.contractAmount) return 0;
      return Math.max(0, Math.min(100, Math.round((d.contractPaid || 0) / d.contractAmount * 100)));
    });
    self.invoiceHint = ko.computed(function () {
      return self.autoInvoice()
        ? 'Generated from the invoice-number template (Module Settings).'
        : 'Required before submission.';
    });

    /* ── load ─────────────────────────────────────────────────────────────── */
    self.reloadAttachments = function () {
      return flService.getVoucherDocuments(id)
        .then(function (items) { self.attachments(items); })
        .catch(function () { self.attachments([]); });
    };

    // A voucher raised before a lookup value was retired still carries that code.
    // KO's options binding nulls a value it cannot find, so re-inject it as its own
    // option rather than silently blanking the field.
    var RETIRED_METHOD = { BANK_TRANSFER: 'Bank Transfer', CHEQUE: 'Cheque', CASH: 'Cash' };
    function keepStored(list, code) {
      if (!code) return;
      var known = list().some(function (o) { return o.code === code; });
      if (!known) list.push({ code: code, name: RETIRED_METHOD[code] || code });
    }

    self.reload = function () {
      flService.getLookups().then(function (l) {
        self.docTypes(l.docTypes || []);
        self.paymentMethods((l.paymentMethods || []).slice());
        self.payGroups((l.payGroups || []).slice());
      }).catch(function () { /* the stored value is re-injected below regardless */ })
        .then(function () { return flService.getVoucher(id); })
        .then(function (v) {
          self.data(v);
          keepStored(self.paymentMethods, v.paymentMethod);
          keepStored(self.payGroups, v.payGroup);
          self.invoiceNumber(v.invoiceNumber || '');
          self.invoiceDate(v.invoiceDate || '');
          self.paymentMethod(v.paymentMethod || '');
          self.notes(v.notes || '');
          self.amount(v.amount != null ? Number(v.amount) : null);
          self.dueDate(v.dueDate || '');
          self.periodLabel(v.periodLabel || '');
          self.description(v.description || '');
          self.payGroup(v.payGroup || '');
          self.loading(false);
          if (self._guard) { self._guard.reset(); }
          else {
            self._guard = formGuard.track([
              self.invoiceNumber, self.invoiceDate, self.paymentMethod, self.notes,
              self.amount, self.dueDate, self.periodLabel, self.description, self.payGroup
            ]);
          }
        })
        .catch(function () { self.loading(false); });

      self.reloadAttachments();
      flService.getSettings().then(function (rows) {
        var s = (rows || []).filter(function (r) { return r.key === 'INVOICE_NUMBER_AUTO'; })[0];
        self.autoInvoice(!s || String(s.value).toUpperCase() !== 'N');
      }).catch(function () { /* default: auto */ });
    };
    self.reload();

    // A draft is editable by whoever raised it; an FL admin can still correct a
    // voucher that is out for approval. Nothing is editable once it is approved,
    // rejected, cancelled or paid.
    self.editable = ko.computed(function () {
      var d = self.data();
      if (d.paymentStatus === 'PAID') return false;
      return d.status === 'DRAFT' || (self.isAdmin && d.status === 'SUBMITTED');
    });
    // The money fields — amount, due date, period — are the admin's alone.
    self.adminEdit = ko.computed(function () { return self.isAdmin && self.editable(); });
    self.adminHint = ko.computed(function () {
      return self.data().status === 'SUBMITTED'
        ? 'This voucher is out for approval — your changes take effect immediately and are audited.'
        : 'Changing the amount, due date or period also updates the payment-schedule row this voucher was generated from.';
    });

    /* ── attachments ──────────────────────────────────────────────────────── */
    self.addAttachment = function () {
      docUpload.choose({ accept: '.pdf,.jpg,.jpeg,.png,.doc,.docx,.xls,.xlsx', maxMb: 10 })
        .then(function (file) {
          if (!file) return;
          // Default to "Supplier Invoice" when the catalogue carries it, else the
          // generic supporting-document type.
          var types = self.docTypes();
          var pick  = types.filter(function (t) { return t.code === 'INVOICE'; })[0]
                   || types.filter(function (t) { return t.code === 'OTHER'; })[0]
                   || types[0];
          if (!pick) { self.errorMsg('No document types are configured.'); return; }
          self.docBusy(true);
          return flService.createDocument({
            sourceType: 'VOUCHER',
            sourceId: id,
            freelancerId: self.data().freelancerId,
            docTypeId: pick.docTypeId || pick.id,
            documentName: file.name,
            mimeType: file.type || 'application/octet-stream'
          }).then(function (r) {
            return flService.uploadDocumentFile(r.documentId, file);
          }).then(function () {
            self.docBusy(false);
            flash(file.name + ' attached.');
            return self.reloadAttachments();
          });
        })
        .catch(function (err) {
          self.docBusy(false);
          self.errorMsg((err && err.message) || 'Attachment failed');
        });
    };
    self.removeAttachment = function (doc) {
      if (!window.confirm('Remove ' + (doc.documentName || 'this attachment') + '?')) return;
      self.docBusy(true);
      flService.deleteDocument(doc.documentId).then(function () {
        self.docBusy(false);
        flash('Attachment removed.');
        return self.reloadAttachments();
      }).catch(function (err) {
        self.docBusy(false);
        self.errorMsg((err && err.message) || 'Could not remove the attachment');
      });
    };
    self.viewAttachment     = function (d) { docFile.view(d); };
    self.downloadAttachment = function (d) { docFile.download(d); };

    /* ── save / submit / pay ──────────────────────────────────────────────── */
    self.saveDraft = function () {
      var payload = {
        invoiceNumber: (self.invoiceNumber() || '').trim(),
        invoiceDate:   self.invoiceDate() || null,
        paymentMethod: self.paymentMethod(),
        notes:         (self.notes() || '').trim()
      };
      if (self.adminEdit()) {
        var amt = Number(self.amount());
        if (!(amt > 0)) {
          self.errorMsg('Amount must be greater than zero.');
          return Promise.reject(new Error('Amount must be greater than zero.'));
        }
        if (!self.dueDate()) {
          self.errorMsg('Due date is required.');
          return Promise.reject(new Error('Due date is required.'));
        }
        payload.amount      = amt;
        payload.dueDate     = self.dueDate();
        payload.periodLabel = (self.periodLabel() || '').trim();
        payload.description = (self.description() || '').trim();
        payload.payGroup    = self.payGroup();
      }
      self.saving(true);
      self.errorMsg('');
      return flService.updateVoucher(id, payload).then(function () {
        self.saving(false);
        flash('Saved.');
        if (self._guard) self._guard.reset();
        // the summary rail and the fund verdict are computed from the saved figures
        self.reload();
      }).catch(function (err) {
        self.saving(false);
        self.errorMsg((err && err.message) || 'Save failed');
        throw err;
      });
    };

    self.submit = function () {
      self.saveDraft().then(function () {
        return flService.submitVoucher(id);
      }).then(function () {
        flash('Submitted for approval.');
        self.reload();
      }).catch(function (err) {
        if (err && err.message) self.errorMsg(err.message);
      });
    };

    self.markPaid = function () {
      flService.markVoucherPaid(id, {}).then(function () {
        flash('Payment confirmed.');
        self.reload();
      }).catch(function (err) { self.errorMsg((err && err.message) || 'Payment confirmation failed'); });
    };

    self.statusBadge = function (s) {
      return { DRAFT: 'badge--draft', SUBMITTED: 'badge--submitted', APPROVED: 'badge--approved',
               REJECTED: 'badge--rejected', CANCELLED: 'badge--cancelled' }[s] || 'badge--draft';
    };
    self.payBadge = function (s) {
      return { PENDING: 'badge--pendingp', PAID: 'badge--paid', CANCELLED: 'badge--cancelled' }[s] || 'badge--pendingp';
    };

    self.openContract = function () {
      sessionStorage.setItem('flContractId', String(self.data().contractId));
      if (window._jetApp) window._jetApp.navigate('contractDetail');
    };
    self.back = function () { if (window._jetApp) window._jetApp.navigate('vouchers'); };
  }

  return VoucherDetailViewModel;
});
