"""otbi-atd : add a new analysis as a job (one command).

Downloads a sample of the analysis (reusing the saved OTBI session), profiles
its columns, and GENERATES two SQL scripts under otbi-atd/db/generated/:
  <table>_table.sql  - CREATE TABLE sized/typed from the live data
  <table>_seed.sql   - MERGE into ATD_OTBI_JOBS (the job row)

Review them, then run via SQLcl (or pass --apply to run them now).

Usage:
  python add_analysis.py "<analysis full path>" [--table NAME] [--job JOBNAME]
         [--load-mode TRUNCATE_INSERT|MERGE] [--key "Header A,Header B"] [--apply]

Env (same as the runner): OTBI_USER, OTBI_PWD, ATD_STATE_DIR.
Optional: OTBI_ANALYTICS_BASE (default the known pod), OTBI_ENV_NAME (default
FUSION_ADGOV), OTBI_TARGET_NAME (default ATD_LOCAL), ATD_SQLCL (default 'sql').
"""
import argparse
import csv
import io
import json
import os
import re
import subprocess
import sys
from datetime import datetime

sys.stdout.reconfigure(encoding="utf-8", errors="replace")
from playwright.sync_api import sync_playwright

import auth
import extract

DB_DIR = os.path.join(os.path.dirname(__file__), "..", "db", "generated")
DEFAULT_BASE = os.environ.get("OTBI_ANALYTICS_BASE",
                              "https://iaaibv.fa.ocs.oraclecloud29.com/analytics")

# Oracle reserved words we must not use as bare column names
RESERVED = {"ORDER", "DATE", "LEVEL", "NUMBER", "COMMENT", "ROW", "ROWID", "SIZE",
            "TABLE", "COLUMN", "ACCESS", "GROUP", "USER", "SESSION", "START", "TO",
            "FROM", "SELECT", "VALUES", "RAW", "DESC", "ASC", "MODE", "UID"}
LEN_BUCKETS = [10, 20, 30, 40, 60, 100, 150, 200, 300, 400, 600, 1000, 2000, 4000]
DATE_RE = re.compile(r"^\d{4}-\d{2}-\d{2}( \d{2}:\d{2}:\d{2})?$")
INT_RE = re.compile(r"^-?\d+$")
NUM_RE = re.compile(r"^-?\d+(\.\d+)?$")


def colname(header, used):
    n = re.sub(r"[^A-Za-z0-9]+", "_", header.strip().upper()).strip("_")
    n = re.sub(r"_+", "_", n)
    if not n or not n[0].isalpha():
        n = "C_" + n
    n = n[:28]
    if n in RESERVED:
        n = n + "_"
    base, i = n, 2
    while n in used:
        n = f"{base[:26]}_{i}"; i += 1
    used.add(n)
    return n


def bucket(maxlen):
    want = max(int(maxlen * 1.5) + 1, 10)
    for b in LEN_BUCKETS:
        if want <= b:
            return b
    return 4000


def infer(values, maxlen):
    ne = [v for v in values if v.strip() != ""]
    if not ne:
        return "VARCHAR2(40)"          # all-null: safe default
    if all(DATE_RE.match(v) for v in ne):
        return "DATE"
    if all(INT_RE.match(v) for v in ne) and maxlen <= 15:
        return "NUMBER"
    if all(NUM_RE.match(v) for v in ne) and maxlen <= 18:
        return "NUMBER"
    return f"VARCHAR2({bucket(maxlen)})"


def profile(csv_text):
    rows = list(csv.reader(io.StringIO(csv_text)))
    if not rows:
        raise SystemExit("analysis returned no rows")
    hdr, data = rows[0], rows[1:]
    used, cols = set(), []
    for i, h in enumerate(hdr):
        vals = [r[i] if i < len(r) else "" for r in data]
        maxlen = max((len(v) for v in vals), default=0)
        cols.append({"header": h, "name": colname(h, used),
                     "type": infer(vals, maxlen), "maxlen": maxlen,
                     "nulls": sum(1 for v in vals if v.strip() == "")})
    return cols, len(data)


def write_sql(table, job, src_path, env_name, target_name, load_mode, key_cols, cols):
    os.makedirs(DB_DIR, exist_ok=True)
    tbl = f"prod.{table.lower()}"
    ddl = [f"-- generated by add_analysis.py for {src_path}",
           "SET DEFINE OFF", "SET SQLBLANKLINES ON", "SET ECHO ON",
           "BEGIN",
           f"  EXECUTE IMMEDIATE 'DROP TABLE {tbl} CASCADE CONSTRAINTS';",
           "EXCEPTION WHEN OTHERS THEN IF SQLCODE != -942 THEN RAISE; END IF;",
           "END;", "/", f"CREATE TABLE {tbl} ("]
    for i, c in enumerate(cols):
        comma = "," if i < len(cols) - 1 else ","
        ddl.append(f"  {c['name']:<26} {c['type']:<16}{comma}  -- {c['header']}")
    ddl.append("  load_ts                    TIMESTAMP DEFAULT SYSTIMESTAMP")
    ddl.append(");")
    ddl.append(f"PROMPT {table} table : done")
    ddl_path = os.path.join(DB_DIR, f"{table.lower()}_table.sql")
    with open(ddl_path, "w", encoding="utf-8", newline="\r\n") as f:
        f.write("\n".join(ddl) + "\n")

    colmap = {c["header"]: c["name"] for c in cols}
    keymap = ",".join(colmap.get(k.strip(), k.strip()) for k in key_cols) if key_cols else "NULL"
    keysql = f"'{keymap}'" if key_cols else "NULL"
    seed = ["SET DEFINE OFF", "SET SQLBLANKLINES ON",
            "ALTER SESSION DISABLE PARALLEL DML;",
            "ALTER TABLE prod.atd_otbi_jobs NOPARALLEL;",
            f"MERGE INTO prod.atd_otbi_jobs t USING (SELECT '{job}' job_name FROM dual) s",
            "  ON (t.job_name = s.job_name)",
            "WHEN MATCHED THEN UPDATE SET",
            f"  env_name='{env_name}', target_name='{target_name}',",
            f"  source_ref='{src_path}', stage_table='PROD.{table.upper()}',",
            f"  load_mode='{load_mode}', key_columns={keysql}, enabled='Y'",
            "WHEN NOT MATCHED THEN INSERT (job_name, env_name, target_name, source_ref,",
            "  output_format, stage_table, load_mode, key_columns, column_map_json, enabled)",
            f"  VALUES ('{job}','{env_name}','{target_name}','{src_path}','csv',",
            f"          'PROD.{table.upper()}','{load_mode}',{keysql},",
            "          '" + json.dumps(colmap, ensure_ascii=False).replace("'", "''") + "','Y');",
            "COMMIT;", f"PROMPT {job} seed : done"]
    seed_path = os.path.join(DB_DIR, f"{table.lower()}_seed.sql")
    with open(seed_path, "w", encoding="utf-8", newline="\r\n") as f:
        f.write("\n".join(seed) + "\n")
    return ddl_path, seed_path


def run_sqlcl(paths):
    sqlcl = os.environ.get("ATD_SQLCL", "sql")
    runner = os.path.join(DB_DIR, "_apply.sql")
    with open(runner, "w", encoding="utf-8", newline="\r\n") as f:
        for p in paths:
            f.write(f"@{os.path.abspath(p)}\n")
        f.write("EXIT;\n")
    print(f"[apply] {sqlcl} -name prod_mcp @{runner}")
    subprocess.run([sqlcl, "-name", "prod_mcp", f"@{runner}"], check=True)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("path")
    ap.add_argument("--table")
    ap.add_argument("--job")
    ap.add_argument("--load-mode", default="TRUNCATE_INSERT",
                    choices=["TRUNCATE_INSERT", "MERGE"])
    ap.add_argument("--key", default="")
    ap.add_argument("--apply", action="store_true")
    a = ap.parse_args()

    leaf = a.path.rstrip("/").split("/")[-1]
    table = (a.table or "ATD_" + re.sub(r"[^A-Za-z0-9]+", "_", leaf).strip("_")).upper()
    job = (a.job or re.sub(r"[^A-Za-z0-9]+", "_", leaf).strip("_")).upper()
    keys = [k for k in a.key.split(",") if k.strip()] if a.key else []

    env = {"env_name": os.environ.get("OTBI_ENV_NAME", "FUSION_ADGOV"),
           "analytics_base_url": DEFAULT_BASE,
           "credential_ref": os.environ.get("OTBI_ENV_NAME", "FUSION_ADGOV")}
    target_name = os.environ.get("OTBI_TARGET_NAME", "ATD_LOCAL")

    with sync_playwright() as p:
        browser, ctx = auth.authenticate(p, env)
        try:
            csv_text = extract.download_csv(ctx, env, a.path)
        finally:
            browser.close()

    cols, n = profile(csv_text)
    print(f"\nanalysis: {a.path}\nrows sampled: {n}\ntable: PROD.{table}   job: {job}   "
          f"load_mode: {a.load_mode}   keys: {keys or '(none)'}\n")
    for c in cols:
        print(f"  {c['header']:34} -> {c['name']:26} {c['type']:14} "
              f"(maxlen={c['maxlen']}, nulls={c['nulls']})")

    ddl_path, seed_path = write_sql(table, job, a.path, env["env_name"],
                                    target_name, a.load_mode, keys, cols)
    print(f"\nwrote:\n  {ddl_path}\n  {seed_path}")
    if a.apply:
        run_sqlcl([ddl_path, seed_path])
        print("\napplied. run:  python runner.py " + job)
    else:
        print("\nreview, then apply with SQLcl:")
        print(f"  sql -name prod_mcp @{os.path.abspath(ddl_path)}")
        print(f"  sql -name prod_mcp @{os.path.abspath(seed_path)}")
        print("or re-run with --apply.  then:  python runner.py " + job)


if __name__ == "__main__":
    main()
