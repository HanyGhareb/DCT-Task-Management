-- =============================================================================
-- ATD Loader (App 208) -- "Generate Schedule OTBI Data" request queue
-- File : 37_atd_schedgen.sql
-- Holds requests to bulk-generate the F/UH/U10M incremental variants for every
-- base analysis under a catalog folder (optionally recursing subfolders),
-- submitted from the OTBI Discovery page. The runner (python runner.py --schedgen)
-- drains QUEUED rows via schedgen.generate(). Status: QUEUED -> RUNNING -> DONE | FAILED.
-- Run : sql -name prod_mcp @37_atd_schedgen.sql   (idempotent)
-- =============================================================================
SET DEFINE OFF
SET SQLBLANKLINES ON

BEGIN
  EXECUTE IMMEDIATE q'[
    CREATE TABLE prod.atd_schedgen_request (
      req_id             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
      folder_path        VARCHAR2(1000) NOT NULL,
      include_subfolders CHAR(1) DEFAULT 'N' NOT NULL,
      status             VARCHAR2(20) DEFAULT 'QUEUED' NOT NULL,
      message            VARCHAR2(4000),
      result_json        CLOB,
      requested_by       VARCHAR2(128),
      created            TIMESTAMP DEFAULT SYSTIMESTAMP,
      started            TIMESTAMP,
      finished           TIMESTAMP
    )
  ]';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE != -955 THEN RAISE; END IF;
END;
/

BEGIN
  EXECUTE IMMEDIATE
    'CREATE INDEX prod.ix_atd_schedgen_status ON prod.atd_schedgen_request (status, req_id)';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE NOT IN (-955, -1408) THEN RAISE; END IF;
END;
/

CREATE OR REPLACE PACKAGE prod.atd_schedgen_pkg AS
  -- atomically claim the next QUEUED request (QUEUED -> RUNNING); NULL when none
  FUNCTION claim_next RETURN NUMBER;
END atd_schedgen_pkg;
/

CREATE OR REPLACE PACKAGE BODY prod.atd_schedgen_pkg AS
  FUNCTION claim_next RETURN NUMBER IS
    CURSOR c IS
      SELECT req_id FROM prod.atd_schedgen_request
       WHERE status = 'QUEUED' ORDER BY req_id
       FOR UPDATE SKIP LOCKED;
    v NUMBER;
  BEGIN
    OPEN c;
    FETCH c INTO v;
    IF c%FOUND THEN
      UPDATE prod.atd_schedgen_request
         SET status = 'RUNNING', started = SYSTIMESTAMP
       WHERE req_id = v;
    END IF;
    CLOSE c;
    COMMIT;
    RETURN v;
  END claim_next;
END atd_schedgen_pkg;
/

-- ADMIN synonyms (ORDS handlers run as ADMIN)
CREATE OR REPLACE SYNONYM admin.atd_schedgen_request FOR prod.atd_schedgen_request;
CREATE OR REPLACE SYNONYM admin.atd_schedgen_pkg     FOR prod.atd_schedgen_pkg;

PROMPT 37_atd_schedgen : done
