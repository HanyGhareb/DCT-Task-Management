-- =============================================================================
-- ATD Loader (App 208) -- analysis build request queue
-- File : 15_atd_analysis_request.sql
-- Holds "build a new OTBI analysis" requests submitted from the Jobs page UI.
-- The runner (python runner.py --build) drains QUEUED rows: it drives the OTBI
-- Answers UI via create_analysis.build_analysis(spec), saves the analysis, then
-- registers it as an ATD job. Status: QUEUED -> BUILDING -> DONE | FAILED.
-- Run : sql -name prod_mcp @15_atd_analysis_request.sql   (idempotent)
-- =============================================================================
SET DEFINE OFF
SET SQLBLANKLINES ON

BEGIN
  EXECUTE IMMEDIATE q'[
    CREATE TABLE prod.atd_analysis_request (
      req_id        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
      spec_json     CLOB NOT NULL,
      analysis_name VARCHAR2(256),
      save_folder   VARCHAR2(512),
      status        VARCHAR2(20) DEFAULT 'QUEUED' NOT NULL,
      message       VARCHAR2(4000),
      job_name      VARCHAR2(128),
      requested_by  VARCHAR2(128),
      created       TIMESTAMP DEFAULT SYSTIMESTAMP,
      finished      TIMESTAMP
    )
  ]';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE != -955 THEN RAISE; END IF;   -- -955 = name already used; keep existing
END;
/

BEGIN
  EXECUTE IMMEDIATE
    'CREATE INDEX prod.ix_atd_anreq_status ON prod.atd_analysis_request (status, req_id)';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE NOT IN (-955, -1408) THEN RAISE; END IF;   -- already exists / already indexed
END;
/

PROMPT 15_atd_analysis_request : done
